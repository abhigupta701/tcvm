class Material():

    def set_items(self,items):
        self.item=items
    def set_itmcapacity(self,itmcap):
        self.itmcap=itmcap
    def set_price(self,pr):
        self.price=pr
    def get_items(self):
        return self.item
    def get_itmcapacity(self):
        return self.itmcap
    def get_price(self):
        return self.price
        
